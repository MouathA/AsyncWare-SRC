package optfine;

public class CustomAnimationFrame
{
    public int counter;
    public int index;
    public int duration;
    
    public CustomAnimationFrame(final int index, final int duration) {
        this.index = 0;
        this.duration = 0;
        this.counter = 0;
        this.index = index;
        this.duration = duration;
    }
}
